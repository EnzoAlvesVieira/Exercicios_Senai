numero = int(input(f'Digite um número: '))
if numero == 1:
    print(f'Domingo')
elif numero == 2:
    print(f'Segunda-Feira')
elif numero == 3:
    print(f'Terça-Feira')
elif numero == 4:
    print(f'Quarta-Feira')
elif numero == 5:
    print('Quinta-Feira')
elif numero == 6:
    print('Sexta-Feira')
elif numero == 7:
    print('Sabado')
else:
    print('Número errado, Digite novamente')